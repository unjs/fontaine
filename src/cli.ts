// CLI setup using commander.js
async function main() {
  const program = new Command();

  program
    .name('fontaine-cli')
    .description('CLI tool for Fontaine font optimization, analyzing URLs and generating font overrides.')
    .version('0.1.0'); // Version should ideally be synced with package.json

  program
    .command('analyze <url>')
    .description('Analyze a URL to detect fonts and generate optimized font overrides. Optionally provide local font files.')
    .option('-l, --local-files <files...>', 'Space-separated list of local font files to consider (e.g., path/to/font.ttf path/to/other.woff)')
    .option('-o, --output-format <format>', 'Output format: "json" or "css" (default: "css")', 'css')
    .action(async (url: string, options: { localFiles?: string[], outputFormat: 'json' | 'css' }) => {
      try {
        // Validate output format explicitly
        if (!['json', 'css'].includes(options.outputFormat)) {
          throw new Error(`Invalid output format: "${options.outputFormat}". Must be "json" or "css".`);
        }

        const analysisOptions: AnalysisOptions = {
          localFiles: options.localFiles,
          outputFormat: options.outputFormat,
        };
        const result = await analyzeFonts(url, analysisOptions);
        console.log(result.overrides); // The primary output of the CLI command
        process.exit(0);
      } catch (error: unknown) {
        if (error instanceof Error) {
          console.error(`Error during analysis: ${error.message}`);
        } else {
          console.error(`An unknown error occurred during analysis: ${String(error)}`);
        }
        process.exit(1);
      }
    });

  // Use parseAsync to correctly handle asynchronous command actions
  await program.parseAsync(process.argv);

  // If no commands or arguments are provided, show help
  if (!process.argv.slice(2).length) {
    program.outputHelp();
  }
}

// Check if the script is run directly (as a CLI)
if (require.main === module) {
  main();
}